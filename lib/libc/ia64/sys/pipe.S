#include <machine/asm.h>
__FBSDID("$FreeBSD: src/lib/libc/ia64/sys/pipe.S,v 1.4 2003/05/16 21:26:39 marcel Exp $");

#include "SYS.h"

ENTRY(__sys_pipe, 1)
	WEAK_ALIAS(pipe, __sys_pipe)
	WEAK_ALIAS(_pipe, __sys_pipe)
	st8		[sp]=r32
	CALLSYS_ERROR(pipe)
	ld8		r14=[sp]
	;;
	st4		[r14]=ret0,4
	;;
	st4		[r14]=ret1
	mov		ret0=0
	br.ret.sptk.few rp
END(__sys_pipe)
