#include "at91rm9200.h"
#include "spi_flash.h"
#include "lib.h"

void
Delay(int us)
{
	unsigned later, now;

	now = AT91C_BASE_ST->ST_CRTR;
	later = (now + us / 25 + 1) & AT91C_ST_CRTV;
	while (later != AT91C_BASE_ST->ST_CRTR)
		continue;
}
