/* AQ2 boot and host request/status registers. */
#define AQ2_MIF_HOST_FINISHED_STATUS_WRITE_REG 0x0e00u
#define AQ2_MIF_HOST_FINISHED_STATUS_READ_REG 0x0e04u
#define AQ2_MIF_HOST_FINISHED_STATUS_ACK BIT(0)

#define AQ2_MCP_HOST_REQ_INT_REG 0x0f00u
#define AQ2_MCP_HOST_REQ_INT_READY BIT(0)
#define AQ2_MCP_HOST_REQ_INT_CLR_REG 0x0f08u

#define AQ2_MIF_BOOT_REG 0x3040u
#define AQ2_MIF_BOOT_REQ_REBOOT BIT(0)
#define AQ2_MIF_BOOT_REQ_HOST_BOOT BIT(8)
#define AQ2_MIF_BOOT_HOST_DATA_LOADED BIT(16)
#define AQ2_MIF_BOOT_BOOT_STARTED BIT(24)
#define AQ2_MIF_BOOT_CRASH_INIT BIT(27)
#define AQ2_MIF_BOOT_BOOT_CODE_FAILED BIT(28)
#define AQ2_MIF_BOOT_FW_INIT_FAILED BIT(29)
#define AQ2_MIF_BOOT_FW_INIT_COMP_SUCCESS BIT(31)

#define AQ2_MIF_BOOT_FW_FAIL_MASK                                  \
	(AQ2_MIF_BOOT_CRASH_INIT | AQ2_MIF_BOOT_BOOT_CODE_FAILED | \
	    AQ2_MIF_BOOT_FW_INIT_FAILED)
#define AQ2_MIF_BOOT_FW_DONE_MASK \
	(AQ2_MIF_BOOT_FW_FAIL_MASK | AQ2_MIF_BOOT_FW_INIT_COMP_SUCCESS)

/* AQ2 hostboot request registers and field masks. */
#define AQ2_HOSTBOOT_REQ_OFFSET_REG 0x0328u
#define AQ2_HOSTBOOT_REQ_LENGTH_REG 0x032cu
#define AQ2_HOSTBOOT_SHARED_IMAGE_BUF 0x10000u
#define AQ2_HOSTBOOT_DATA_OFFSET_REG 0x12a4cu
