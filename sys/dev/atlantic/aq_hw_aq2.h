#define AQ2_HOSTBOOT_ITI_REQ_MASK 0x00100000u
#define AQ2_HOSTBOOT_OFFSET_MASK 0x7ffffu
#define AQ2_HOSTBOOT_LENGTH_MASK 0x0fffu

